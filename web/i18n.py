# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------

def detect_language(request: Request) -> str:
    """Return ``"en"`` or ``"zh"``.

    Order of precedence:
      1. The ``lang`` cookie (user clicked the flag at least once).
      2. The first ``Accept-Language`` tag — any ``zh*`` wins.
      3. English default.
    """
    pref = request.cookies.get("lang", "").strip().lower()
    if pref in ("en", "zh"):
        return pref
    accept = request.headers.get("accept-language", "").lower()
    # Browsers list tags in preference order; q-values are skipped because the
    # first-wins behavior matches every real browser we care about.
    for tag in accept.split(","):
        head = tag.strip().split(";", 1)[0].strip()
        if head.startswith("zh"):
            return "zh"
    return "en"


def translate(lang: str, key: str) -> str:
    """Look up ``key`` in ``lang``. Fall back to English, then to the key itself.

    Returning the raw key on miss makes missing translations visible in the
    rendered DOM, which is the easiest way to catch them during the sweep.
    """
    table = STRINGS.get(lang) or STRINGS["en"]
    val = table.get(key)
    if val is not None:
        return val
    en = STRINGS["en"].get(key)
    if en is not None:
        return en
    return key


def flag_for_destination(lang: str) -> str:
    """Flag of the *other* language — the one a click on the button switches to."""
    return FLAG_UK if lang == "zh" else FLAG_CN


def html_lang_attr(lang: str) -> str:
    """BCP 47 tag for the ``<html lang>`` attribute. ``zh-CN`` is standard."""
    return "zh-CN" if lang == "zh" else "en"


